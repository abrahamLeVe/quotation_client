export function processQuery(query: string) {
  const firstFilter = query.toLowerCase();
  const keywords = firstFilter
    .replace(/[.,]/g, "")
    .slice(0, 40)
    .split(" ")
    .filter((word) => !wordsExclude.has(word));

  return keywords
    .map((word) => word.replace(/s$|es$/, ""))
    .filter((element) => element.length >= 3);
}

